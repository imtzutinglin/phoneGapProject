        <h4>高血壓</h4>
        <p>疾病介紹:</p>
        <p>是一種普遍長期潛伏體內的慢性病，發病後不易根治控制不當易發生合併症、殘障甚至死亡。因此早期發現並給予持續適當的控制與保養為當務之急。</p>
        <p>飲食禁忌:</p>
        <p>1、高熱能食物可誘發肥胖，肥胖者高血壓發病率比正常體重者高。</p>
        <p>2、高鹽食物食鹽的主要成分是氯化鈉，鈉溜留可引起細胞外液增加，心排出量增多，血壓上升。因此，高血壓患者應限制鹽量的攝入。</p>
         <p>3、除此之外，還有重口味零食、刺激性食物、使神經興奮食物、高糖、高膽固醇食物。</p>
