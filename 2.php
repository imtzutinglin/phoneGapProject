        <h4>關節退化</h4>
        <p>疾病介紹:</p>
        <p>主要病變的地方是兩個骨頭的關節接觸面，所謂的關節軟骨受到傷害及磨損，關節表面凹凸不平和關節腔變窄以及骨刺的產生。退化性關節炎主要的症狀是疼痛、僵硬、腫大和變形，在活動關節時，常可聽到不正常的磨擦聲響。</p>
    <p>飲食禁忌:</p>
        <p>1、少吃含油量較高的食物即可。</p>
         <p>2、膠原蛋白是關節的重要成分，但豬腳的膠質雖多，卻沒有研究證實能直接對關節有益。</p>
