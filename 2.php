        <h4>心臟病</h4>
        <p>疾病介紹:</p>
        <p>當身體的心輸出量減少或者是血液容積下降至造成身體組織缺氧時，就會造成休克的情形發生，它的特徵是蒼白，皮膚濕冷，耳朵及手指發紺，脈搏快速但微弱，呼吸快且淺，體溫降低，以及精神錯亂情形，甚至無意識。</p>
        <p>飲食禁忌:</p>
        <p>1、適量的限制食鹽的攝入。</p>
        <p>2、戒刺激性食飲和興奮性藥物。</p>
        <p>3、應吃易於消化的食物。</p>
        <p>4、應供給充足的維生素和適量的無機鹽。</p>
        <p>5、緩進飲料。</p>
