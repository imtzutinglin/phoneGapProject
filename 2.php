        <h4>糖尿病</h4>
        <p>疾病介紹:</p>
        <p>1.老年糖尿病絕大多數為2型糖尿病流行病學資料表明，2型糖尿病患病率隨增齡而上升。國外報告，65歲以上人群的2型糖尿病和糖耐量減低（IGT，即空腹血糖<7.0毫摩爾/升，餐後2小時血糖在7.8~11.1毫摩爾/升）的患病率為10%~20%。</p>
        <p>2.老年糖尿病多數起病緩慢多無症狀，往往由於常規體檢或因其他疾病檢查血糖或尿糖時發現。</p>
        <p>3.部分老年糖尿病以併發症為首發表現如高血糖高滲狀態，心、腦血管意外以及視力改變等。</p>
        <p>4.少數老年糖尿病患者有其他表現如體溫低、多汗、神經性惡病質、肌肉萎縮和認知功能減退等。</p>
        <p>飲食禁忌:</p>
        <p>1、吃糖太多會發病。</p>
        <p>2、不宜吃碳水化合物</p>
        <p>3、不能吃自己最喜歡的食物。</p>
        <p>4、蛋白質比碳水化合物好。</p>
        <p>5、可以隨意根據食量調節用藥量。</p>
        <p>6、與甜品絕緣。</p>
        <p>7、吃鹹的血糖不會升高。</p>
        <p>8、要單獨分餐。</p>
