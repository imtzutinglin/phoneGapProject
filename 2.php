        <h4>白內障</h4>
        <p>疾病介紹:</p>
        <p>當水晶體因任何原因使可溶性蛋白質逐漸變成不可溶性蛋白質而形成混濁，導致視力模糊就稱為白內障(附圖)。</p>
        <p>飲食禁忌:</p>
        <p>1、油炸食品以及人造脂肪、人造黃油、動物脂肪（因為這些食物會加速氧化反應）。</p>
         <p>2、全脂奶粉、牛奶、奶油、奶酪、冰淇淋等含乳糖豐富的乳製品。</p>
