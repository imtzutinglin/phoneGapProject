        <h4>痛風</h4>
        <p>疾病介紹:</p>
        <p>一種常見的關節發炎疾病，主要原因是尿酸的結晶沈積在關節內，進而產生嚴重紅、腫、熱、痛的發炎反應。痛風問題最常發生在足部，但其實在腳踝、腳背、腳跟、膝蓋、手腕…等部位也都可能發作，而且在發作時會產生難以忍受的疼痛，沒真的痛過的人大概很難想像。</p>
        <p>飲食禁忌:</p>
        <p>高普林食物、酒精、含糖飲料、精緻食物、油炸、高油脂食物、乾香菇、紫菜。</p>
